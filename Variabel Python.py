# Proses memasukkan data ke dalam variabel
nama_hewan = "Kucing"
# Proses mencetak variabel
print(nama_hewan)

# Nilai dan tipe data dalam variabel dapat diubah
umur_hewan = 3               # Nilai awal
print(umur_hewan)            # Mencetak nilai umur_hewan
print(type(umur_hewan))      # Mengecek tipe data umur_hewan
umur_hewan = "tiga tahun"    # Nilai setelah diubah
print(umur_hewan)            # Mencetak nilai umur_hewan
print(type(umur_hewan))      # Mengecek tipe data umur_hewan

