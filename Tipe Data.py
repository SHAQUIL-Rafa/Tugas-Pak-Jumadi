# Tipe data integer
angka = 10
print("Tipe data integer:", angka)

# Tipe data float
desimal = 10.5
print("Tipe data float:", desimal)

# Tipe data string
teks = "Hello, World!"
print("Tipe data string:", teks)

# Tipe data boolean
benar = True
salah = False
print("Tipe data boolean:", benar, salah)

# Tipe data list
daftar = [1, 2, 3, 4, 5]
print("Tipe data list:", daftar)

# Tipe data tuple
tuple_data = (1, 2, 3)
print("Tipe data tuple:", tuple_data)

# Tipe data set
set_data = {1, 2, 3, 4, 5}
print("Tipe data set:", set_data)

# Tipe data dictionary
dictionary = {"nama": "John", "umur": 30}
print("Tipe data dictionary:", dictionary)

# Menampilkan tipe data dari masing-masing variabel
print("Tipe data dari 'angka':", type(angka))
print("Tipe data dari 'desimal':", type(desimal))
print("Tipe data dari 'teks':", type(teks))
print("Tipe data dari 'benar':", type(benar))
print("Tipe data dari 'daftar':", type(daftar))
print("Tipe data dari 'tuple_data':", type(tuple_data))
print("Tipe data dari 'set_data':", type(set_data))
print("Tipe data dari 'dictionary':", type(dictionary))
